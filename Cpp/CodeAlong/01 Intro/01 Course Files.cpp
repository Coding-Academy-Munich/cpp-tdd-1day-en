// # Course Files
//
// - On the course's GitHub page you will find the course materials
// - There are two important folders:
//   - `Html` contains static versions of the slides
//   - `code` contains CMake projects with code for examples and larger workshops
// - Two other folders are not essential for this course
//   - `Notebooks` contains the interactive version of the slides
//   - `Cpp` contains the slides as C++ files to make it easy to copy code snippets
// - You can also download a ZIP archive with all materials from the GitHub page

// %% [markdown]
//
// ### The HTML pages
//
// - Contain exactly the same content as the interactive slides used in the
//   presentation
// - Are static
// - Can be viewed in the browser

// %% [markdown]
//
// ## Structure of the `Html` (and `Notebooks`/`Cpp`) folders
//
// - Each of these folders contains two subfolders
//  - `Slides`
//    - Here the completed slides and workshops with solutions are included
//    - These slides correspond to the final state of the respective presentation
//  - `CodeAlong`
//    - Here large parts of the code are missing and the solutions to workshops are missing
//    - This is the state of the slides at the beginning of the presentation
//    - Use the code-alongs as a starting point for the workshops!

// %% [markdown]
//
// ## Structure of the `code` folder
//
// - Here too there are two important subfolders
//   - `starter_kits`
//     - Project skeletons that are used, for example, to solve workshops
//   - `completed`
//     - Complete projects, for example, solutions to workshops
// - The folders are structured as CMake projects
